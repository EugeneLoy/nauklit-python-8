# За означенням: 0! = 1.
#
# Наприклад:
# * для n = 0, факторіал = 1
# * для n = 1, факторіал = 1
# * для n = 2, факторіал = 1*2 = 2
# * для n = 3, факторіал = 1*2*3 = 6
# * для n = 4, факторіал = 1*2*3*4 = 24


